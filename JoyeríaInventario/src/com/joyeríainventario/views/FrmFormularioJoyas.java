    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        // Obtenemos la joya seleccionada
        String materialBusqueda = TxtMaterial.getText().trim();

        // verificamos si se ha seleccionado una fila
        if (materialBusqueda.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese el material de la joya para eliminar");
            return;
        }

        try {
            // buscamos la joya por el material
            Joya joya = eboTabla.buscarPorMaterial(materialBusqueda);

            // verificamos si se encontro la joya
            if (joya == null) {
                JOptionPane.showMessageDialog(null, "No se encontró ninguna joya con el material: " + materialBusqueda);
                return;
            }

            // confirmación de eliminación
            int confirmacion = JOptionPane.showConfirmDialog(null,
                    "¿Estás seguro que desea eliminar esta Joya?",
                    "Confirmar Eliminación",
                    JOptionPane.YES_NO_OPTION);

            if (confirmacion == JOptionPane.YES_OPTION) {
                try {
                    // eliminamos la joya 
                    eboTabla.eliminarJoya(joya.getIdJoya());

                    // actualizamos la tabla
                    listarJoya();
                    JOptionPane.showMessageDialog(null, "Joya eliminada exitosamente");

                    // limpiamos los campos del formulario
                    limpiarCampos();
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null,
                            "Error al eliminar una joya: " + ex.getMessage(),
                            "Error",
                            JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,
                    "Error al eliminar una joya: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

    private void BtnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnBuscarActionPerformed
        /// buscamos una joya por el id para actualizarlo
        String materialBusqueda = TxtBuscarJoya.getText().trim();

        if (materialBusqueda.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese un material para buscar");
            return;
        }

        try {
            // buscamos por el material ingreado
            Joya joya = eboTabla.buscarPorMaterial(materialBusqueda);

            // verificamos si se encontro la joya
            if (joya != null) {
                TxtNombre.setText(joya.getNombre());
                TxtMaterial.setText(joya.getMaterial());
                TxtPeso.setText(String.valueOf(joya.getPeso()));
                TxtPrecio.setText(String.valueOf(joya.getPrecio()));
                SpStock.setValue(joya.getStock());

                JOptionPane.showMessageDialog(null, "Joya encontrado");
                TxtBuscarJoya.setText("");
            } else {
                JOptionPane.showMessageDialog(null,
                        "No se encontró ninguna joya con el material: " + materialBusqueda,
                        "Busqueda sin resultados",
                        JOptionPane.INFORMATION_MESSAGE);
                limpiarCampos();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,
                    "Error al buscar joya: " + ex.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_BtnBuscarActionPerformed

