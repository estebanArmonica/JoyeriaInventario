    private void btnAgregarJoyaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarJoyaActionPerformed
        // agregamos una joya al inventario
        if (TxtNombre.getText().isEmpty() || TxtMaterial.getText().isEmpty()
                || TxtPeso.getText().isEmpty() || TxtPrecio.getText().isEmpty()
                || SpStock.getValue() == null) {
            JOptionPane.showMessageDialog(null, "Se necesita llenar todos los campos, por favor ingrese los datos");
        } else {
            Joya joya = new Joya();
            joya.setNombre(TxtNombre.getText());
            joya.setMaterial(TxtMaterial.getText());
            joya.setPeso(Double.parseDouble(TxtPeso.getText()));
            joya.setPrecio(Double.parseDouble(TxtPrecio.getText()));
            joya.setStock((Integer) SpStock.getValue());

            JoyaDAO agregar = new JoyaDAO();

            try {
                agregar.agregarJoya(joya);
                limpiarCampos();
                listarJoya();
            } catch (Exception ex) {
                Logger.getLogger(FrmFormularioJoyas.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnAgregarJoyaActionPerformed

